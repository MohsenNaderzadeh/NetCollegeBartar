public class LastNews {
    private int LastNewsID;
    private String LastNewsLogo;
    private String LastNewsTitle;
    private String LastNewsText;
    private String LastNewsDate;

    public int getLastNewsID() {
        return LastNewsID;
    }

    public void setLastNewsID(int lastNewsID) {
        LastNewsID = lastNewsID;
    }

    public String getLastNewsLogo() {
        return LastNewsLogo;
    }

    public void setLastNewsLogo(String lastNewsLogo) {
        LastNewsLogo = lastNewsLogo;
    }

    public String getLastNewsTitle() {
        return LastNewsTitle;
    }

    public void setLastNewsTitle(String lastNewsTitle) {
        LastNewsTitle = lastNewsTitle;
    }

    public String getLastNewsText() {
        return LastNewsText;
    }

    public void setLastNewsText(String lastNewsText) {
        LastNewsText = lastNewsText;
    }

    public String getLastNewsDate() {
        return LastNewsDate;
    }

    public void setLastNewsDate(String lastNewsDate) {
        LastNewsDate = lastNewsDate;
    }
}
